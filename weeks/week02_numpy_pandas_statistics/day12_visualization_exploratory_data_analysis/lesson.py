# 4. 여러 행에서 같은 열의 값만 모읍니다.
#    일차별 주제가 달라도 "입력 -> 중간 값 -> 출력" 흐름을 찾는 연습은 같습니다.

    # 시각화는 그림을 그리기 전에도 분포와 이상값을 읽는 과정에서 시작한다.
values = [82, 91, 94, 107, 118, 165]
bins = {"under_100": 0, "100_to_139": 0, "140_or_more": 0}
for value in values:
    if value < 100:
        bins["under_100"] += 1
    elif value < 140:
        bins["100_to_139"] += 1
    else:
        bins["140_or_more"] += 1

print("histogram bins:", bins)
print("max value:", max(values))
print("reading task:", "그래프는 예쁜 그림이 아니라 분포와 이상한 값을 찾는 도구이다.")

